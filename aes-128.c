void Sub_Bytes()		//字节替换
{
    int i,j;
    for(i=0;i<4;i++)
	{
		for(j=0;j<4;j++)
        {
            state[i][j]=sBox[state[i][j]];
        }
	}
}


void Shift_Rows()	//行移位变换
{
	int i,j;
	unsigned char temp[4];
	for(i=1;i<4;i++)
	{
		for(j=0;j<4;j++)
		{
			temp[j]=state[i][j];
		}
		for(j=0;j<4;j++)
		{
			state[i][j]=temp[(j+i)%4];
		}
	}
}


void Mix_Columns()	//列混淆变换
{
    int i,j;
	unsigned char temp[4];
	for(j=0;j<4;j++)
	{
		for(i=0;i<4;i++)
		{
			temp[i]=state[i][j];
		}
		for(i=0;i<4;i++)
		{
			state[i][j]=Gf_256(0x02,temp[i])^Gf_256(0x03,temp[(1+i)%4])^Gf_256(0x01,temp[(2+i)%4])^Gf_256(0x01,temp[(3+i)%4]);
		}
	}
}


unsigned char Gf_256(unsigned char a,unsigned char b)	//有限域GF(28)上的乘法
{
	unsigned char temp[3];
	unsigned char res;
	temp[0]=b;		//temp[0]=0x01*b
	temp[1]=b<<1;	//temp[1]=0x02*b
	if(b&0x80)	    //0x80=1000 0000
	{
		temp[1]=temp[1]^0x1b;
	}
	temp[2]=temp[0]^temp[1];	//temp[2]=0x03*b
	if(a==0x01) {res=temp[0];}
	if(a==0x02) {res=temp[1];}
	if(a==0x03) {res=temp[2];}
	return res;
}


void Add_Round_Key()		//轮密钥加变换
{
	int i,j;
	for(j=0;j<4;j++)
	{
		for(i=0;i<4;i++)
		{
			state[i][j]=state[i][j]^cipher_key[i][j];
		}
	}
}


void Key_Expansion(int time)	//aes-128为11组纶密钥，第0组为输入密钥本身
{
	int i,j;
	unsigned char temp[4];
	for(i=0;i<4;i++)	//取出最后一列，同时循环左移一个字节
	{
		temp[i]=cipher_key[(i+1)%4][3];
	}
	for(i=0;i<4;i++)		//取出的一列进行subbytes
	{
		temp[i]=sBox[temp[i]];
	}
	for(i=0;i<4;i++)		//生成下一组密钥第一列
	{
		cipher_key[i][0]=cipher_key[i][0]^temp[i]^Rcon[i][time];
	}
	for(j=1;j<4;j++)		//生成下一组密钥的234列
	{
		for(i=0;i<4;i++)
		{
			cipher_key[i][j]=cipher_key[i][j]^cipher_key[i][j-1];
		}
	}
}
